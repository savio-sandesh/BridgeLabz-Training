// Arithmetic operators 

using System;
class ArithmeticOperators{
			static void Main(){
			
			int a=10;
			int b=5;
			
			// addition
			int add=a+b;
			
			// subtraction
			int sub=a-b;
			
			// Multiplication
			int multiple=a*b;
			
			// Division
			int div=a/b;
			
			// modulus
			int mod=a%b;
			
			Console.WriteLine(add);
			Console.WriteLine(sub);
			Console.WriteLine(multiple);
			Console.WriteLine(div);
			Console.WriteLine(mod);
			}
}